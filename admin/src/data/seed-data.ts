export const getSeedLeagues = () => {
  const now = new Date();
  
  const getStartOfWeek = () => {
    const start = new Date(now);
    start.setDate(now.getDate() - now.getDay());
    start.setHours(0,0,0,0);
    return start.toISOString();
  };
  
  const getEndOfWeek = () => {
    const end = new Date(now);
    end.setDate(now.getDate() + (6 - now.getDay()));
    end.setHours(23,59,59,999);
    return end.toISOString();
  };
  
  const getCurrentWeekKey = () => {
    const year = now.getFullYear();
    const startDate = new Date(now.getFullYear(), 0, 1);
    const days = Math.floor((now.getTime() - startDate.getTime()) / (24 * 60 * 60 * 1000));
    const weekNumber = Math.ceil((now.getDay() + 1 + days) / 7);
    return `${year}-W${weekNumber.toString().padStart(2, '0')}`;
  };

  return ['bronze', 'silver', 'gold', 'sapphire', 'onyx', 'diamond'].map(tier => ({
    tier,
    week_key: getCurrentWeekKey(),
    starts_at: getStartOfWeek(),
    ends_at: getEndOfWeek(),
    member_count: Math.floor(Math.random() * 50) + 10,
    is_active: true,
    is_finalized: false
  }));
};
